package com.example.bean;

public class GridItem {
	private String gridName;
	private String gridImgUrl;
	private int gridImgDrawable;
	
	public String getGridName() {
		return gridName;
	}
	public void setGridName(String gridName) {
		this.gridName = gridName;
	}
	public String getGridImgUrl() {
		return gridImgUrl;
	}
	public void setGridImgUrl(String gridImgUrl) {
		this.gridImgUrl = gridImgUrl;
	}
	public int getGridImgDrawable() {
		return gridImgDrawable;
	}
	public void setGridImgDrawable(int gridImgDrawable) {
		this.gridImgDrawable = gridImgDrawable;
	}

}
